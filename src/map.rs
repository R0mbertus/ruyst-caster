pub fn wall_point(x: usize, y: usize) -> bool {
    match MAP.get(y) {
        Some(row) =>    *row.get(x).unwrap() != 0,
        None =>         false,
    }
}

pub fn block_size(width: f64, height: f64) -> (f64, f64) {
    (width / (WIDTH as f64), height / (HEIGHT as f64))
}
